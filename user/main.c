/*
*************************************************************************
*                             包含的头文件
*************************************************************************
*/
#include <stdint.h>

/*
*************************************************************************
*                              全局变量
*************************************************************************
*/

uint32_t flag1;
uint32_t flag2;


void delay(uint32_t count)
{
	for(;count!=0;count--);
	
}


/*
************************************************************************
*                                main函数
************************************************************************
*/
/*
* 注意事项：1、该工程使用软件仿真，debug需选择 Ude Simulator
*           2、在Target选项卡里面把晶振Xtal(Mhz)的值改为25，默认是12，
*              改成25是为了跟system_ARMCM3.c中定义的__SYSTEM_CLOCK相同，确保仿真的时候时钟一致
*/
int main(void)
{	
	
   
    
    for(;;)
	{
		flag1 = 1;
		delay(100);
		flag1 = 0;
		delay(100);
		
		flag2 = 1;
		delay(100);
		flag2 = 0;
		delay(100);
		
	}
}


